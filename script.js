function quizt(frame) {
  for (let i = 1; i <= 8; i++) {
    document.getElementById('f' + i).style.display = 'none';
  }

  if (frame >= 1 && frame <= 8) {
    document.getElementById('f' + frame).style.display = 'block';
  } else {
    alert('error');
  }
}

